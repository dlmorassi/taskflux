function cad(){
    const lg = document.getElementById("login")
    const cd = document.getElementById("cadastro")
    cd.style.display = "block"
    lg.style.display = "none"
}

function log(){
    const lg = document.getElementById("login")
    const cd = document.getElementById("cadastro")
    cd.style.display = "none"
    lg.style.display = "block"
}